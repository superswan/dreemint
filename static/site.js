// Min-DAll-E Generator
function generate() {
    var r = new XMLHttpRequest();
    r.open("POST", "/generate", true);
    r.onreadystatechange = function () {
    if (r.readyState != 4 || r.status != 200) return;
        var img_obj = JSON.parse(r.responseText)
        var image_showcase = document.getElementById('generated-image')
        var prompt_text = document.getElementById('prompt-text')
        console.log(img_obj.filename)
        image_showcase.src='static/generated/'+img_obj.filename
        prompt_text.textContent=img_obj.prompt
    };
    r.setRequestHeader('content-type', 'application/x-www-form-urlencoded;charset=UTF-8');
    r.send("prompt=" + document.getElementById('prompt').value);
}

function mint() {
    var image = document.getElementById('generated-image').getAttribute('src').replace('static/generated/', '')
    var prompt = document.getElementById('prompt-text').textContent
    var r = new XMLHttpRequest();
    r.open("POST", "/mint", true);
    r.onreadystatechange = function () {
    if (r.readyState != 4 || r.status != 200) return;
        var metadata_cid = r.responseText
        console.log(metadata_cid)
        
        const provider = new ethers.providers.Web3Provider(window.ethereum)
        const signer = provider.getSigner();
        const contract = new ethers.Contract(contractAddress, contractABI, signer)

        const connection = contract.connect(signer)
        const addr = connection.address;
        const result = contract.payToMint(addr, metadata_cid, {
          value: ethers.utils.parseEther('0.01') 
        });

        console.log(result)
    };
    console.log("Minting image from file " + image)
    r.setRequestHeader('content-type', 'application/x-www-form-urlencoded;charset=UTF-8');
    var send_data =  
          'filename=' + image 
        + '&prompt=' + prompt 
    r.send(send_data);

}